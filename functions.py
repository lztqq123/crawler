def get_xml(wb_data,xpath):
    """获取xpath匹配结果
    :param wb_data:
    :param xpath:
    :return:
    """
    xml = etree.HTML(wb_data)
    xpath_list = xml.xpath(xpath)
    return xpath_list

def myjoin(base, url):
    url1 = urljoin(base, url)
    arr = urlparse(url1)
    path = normpath(arr[2])
    return urlunparse((arr.scheme, arr.netloc, path, arr.params, arr.query, arr.fragment))

#header转换
def get_headers(data:str):
    data_dict = dict()
    data = data.split('\n')
    for a in data:
        b = a.split(':')
        data_dict[b[0]] = b[1].strip()
    return data_dict
